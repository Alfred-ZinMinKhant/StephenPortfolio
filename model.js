// Attach modal logic for starfish card only (as example)
document.addEventListener('DOMContentLoaded', function () {
    Object.keys(modalData).forEach(cardKey => {
        document.querySelectorAll(`.portfolio-item[data-category="${cardKey}"]`).forEach((card, cardIdx) => {
            const modal = card.querySelector('.portfolio-modal');
            const slideshowImages = modal.querySelector('.slideshow-images');
            const slideshowRadios = modal.querySelector('.slideshow-radios');
            const description = modal.querySelector('.modal-description');
            const prevBtn = modal.querySelector('.prev-slide');
            const nextBtn = modal.querySelector('.next-slide');
            const tagButtons = card.querySelectorAll('.tag-btn');
            let currentSlide = 0;
            let currentKey = 'highlighted';

            function renderSlideshow(slides) {
                if (currentKey === 'achievements') {
                    if (slideshowImages) slideshowImages.innerHTML = '';
                    if (slideshowRadios) slideshowRadios.innerHTML = '';
                    if (description) description.innerHTML = slides[currentSlide].content;
                    if (prevBtn) prevBtn.style.display = 'none';
                    if (nextBtn) nextBtn.style.display = 'none';
                } else {
                    if (slideshowImages) slideshowImages.innerHTML = `<img src="${slides[currentSlide].image}" style="max-width:85%;margin:0 auto;height:100%;object-fit:cover;border-radius:12px;box-shadow:0 2px 12px rgba(52,69,81,0.10);margin-bottom:1rem;display:block;cursor:zoom-in;" alt="Slideshow Image" class="slideshow-lightbox-img" />`;
                    if (slideshowRadios) slideshowRadios.innerHTML = slides.map((slide, idx) => `
                        <span class="slideshow-dot" style="display:inline-block;width:14px;height:14px;margin:0 6px;border-radius:50%;background:${idx === currentSlide ? 'var(--accent-color, #3451a1)' : '#e0e3ea'};box-shadow:0 1px 4px rgba(52,69,81,0.10);cursor:pointer;transition:background 0.2s;border:2px solid ${idx === currentSlide ? 'var(--accent-color, #3451a1)' : '#cfd3db'};" data-slide="${idx}"></span>
                    `).join('');
                    if (description) description.innerHTML = slides[currentSlide].content;
                    if (prevBtn) prevBtn.style.display = '';
                    if (nextBtn) nextBtn.style.display = '';
                }
            }

            tagButtons.forEach(btn => {
                btn.addEventListener('click', function () {
                    const key = btn.getAttribute('data-modal');
                    if (modalData[cardKey][key]) {
                        currentKey = key;
                        currentSlide = 0;
                        renderSlideshow(modalData[cardKey][key].slides);
                        modal.style.display = 'block';
                        tagButtons.forEach(b => b.classList.remove('active'));
                        btn.classList.add('active');
                    }
                });
            });

            if (prevBtn) {
                prevBtn.onclick = function () {
                    if (currentSlide > 0) {
                        currentSlide--;
                        renderSlideshow(modalData[cardKey][currentKey].slides);
                    }
                };
            }
            if (nextBtn) {
                nextBtn.onclick = function () {
                    if (currentSlide < modalData[cardKey][currentKey].slides.length - 1) {
                        currentSlide++;
                        renderSlideshow(modalData[cardKey][currentKey].slides);
                    }
                };
            }
            if (slideshowRadios) {
                slideshowRadios.addEventListener('click', function (e) {
                    if (e.target.classList.contains('slideshow-dot')) {
                        currentSlide = parseInt(e.target.getAttribute('data-slide'));
                        renderSlideshow(modalData[cardKey][currentKey].slides);
                    }
                });
            }

            // Lightbox logic (shared overlay)
            if (!document.getElementById('lightbox-overlay')) {
                document.body.insertAdjacentHTML('beforeend', `
                    <div id="lightbox-overlay" style="display:none;position:fixed;top:0;left:0;width:100vw;height:100vh;background:rgba(20,24,32,0.92);z-index:9999;justify-content:center;align-items:center;">
                        <span id="lightbox-close" style="position:absolute;top:32px;right:48px;font-size:2.5rem;color:#fff;cursor:pointer;z-index:10001;">&times;</span>
                        <img id="lightbox-img" src="" style="max-width:90vw;max-height:80vh;border-radius:18px;box-shadow:0 8px 32px rgba(52,69,81,0.25);border:4px solid #fff;" />
                    </div>
                `);
            }
            modal.addEventListener('click', function (e) {
                if (e.target.classList.contains('slideshow-lightbox-img')) {
                    const imgSrc = e.target.getAttribute('src');
                    const overlay = document.getElementById('lightbox-overlay');
                    const lightboxImg = document.getElementById('lightbox-img');
                    lightboxImg.src = imgSrc;
                    overlay.style.display = 'flex';
                }
            });
            document.getElementById('lightbox-close').onclick = function () {
                document.getElementById('lightbox-overlay').style.display = 'none';
            };
            document.getElementById('lightbox-overlay').onclick = function (e) {
                if (e.target.id === 'lightbox-overlay') {
                    document.getElementById('lightbox-overlay').style.display = 'none';
                }
            };

            // Show default modal on load
            renderSlideshow(modalData[cardKey]['highlighted'].slides);
            modal.style.display = 'block'; // Show all modals by default
        });
    });

    });

